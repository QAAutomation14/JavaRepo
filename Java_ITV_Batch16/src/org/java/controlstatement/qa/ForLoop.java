package org.java.controlstatement.qa;

public class ForLoop {

	public static void main(String[] args) {

		// PS- Print 1-10

		System.out.println("Print 1-10");
		for (int a = 1; a <= 10; a++) {

			System.out.println(a);
		}

		// PS- Print 10-1

		System.out.println("Print 10-1");
		
		for(int a=10; a>=1; a--) {
			
			System.out.println(a);
		}
		

	}

}
