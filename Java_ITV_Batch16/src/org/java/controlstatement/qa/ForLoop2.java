package org.java.controlstatement.qa;

public class ForLoop2 {

	public static void main(String[] args) {

		// PS- Print 1-10

		System.out.println("Print 1-10");

		int a;

		for (a = 1; a <= 10; a++) {

			System.out.println(a*14);
		}

		// PS- Print 10-1

		System.out.println("Print 10-1");

		int b;

		for (b = 10; b >= 1; b--) {

			System.out.println(b*14);
		}

	}

}
