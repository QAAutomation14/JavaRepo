package org.java.controlstatement.qa;

public class doWhileLoop {

	public static void main(String[] args) {

		System.out.println("Print 1-10");

		int a = 1;

		while (a <= 10) {

			System.out.println(a);
			++a;
		}

		System.out.println("Print 10-1");

		int b = 10;

		while (b >= 1) {

			System.out.println(b);
			--b;
		}

	}

}
